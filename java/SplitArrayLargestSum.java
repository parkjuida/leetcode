    public int fillHistory(int[][] history, int[] nums, int index, int currentSum, int m) {
        if(history[index][m] > -1) {
            return history[index][m];
        }
        if(m == 1) {
            int sum = 0;
            for(int i = index; i < nums.length; i++) sum += nums[i];
            history[index][m] = sum;
        } else {
            int thisValue, minValue = Integer.MAX_VALUE;
            int currentStatus = currentSum;
            for (int i = index + 1; i < nums.length; i++) {
                currentStatus += nums[i - 1];
                thisValue = Math.max(currentStatus, fillHistory(history, nums, i, 0, m - 1));
                minValue = Math.min(minValue, thisValue);
            }
            history[index][m] = minValue;
        }
        return history[index][m];
    }

